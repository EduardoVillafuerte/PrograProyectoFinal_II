    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String nombre = jComboBox1.getSelectedItem().toString();
            if(nombre.isBlank())
                JOptionPane.showMessageDialog(null, "Seleccione un articulo", "Error", JOptionPane.ERROR_MESSAGE);
            else{
                Articulo articulo = hotel.buscarArticulo(nombre);
                articulo.setCantidadDisponible(Integer.parseInt(jTextField2.getText())+articulo.getCantidadDisponible());
                List<Articulo> articuloLs = new ArrayList();
                articuloLs.add(articulo);
                hotel.guardarCambiosInventario(articuloLs, "ProductoUsuarios");
                this.dispose();
                new JFrameAdmin(hotel).setVisible(true);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Ingrese solo n√∫meros en cantidad", "Error", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

